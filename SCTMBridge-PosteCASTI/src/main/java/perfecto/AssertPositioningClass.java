package perfecto;

public class AssertPositioningClass {
	
	private String ID;
	private int position;
	private int commandsArrayNumber;
	
	public AssertPositioningClass() {
		
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public int getPosition() {
		return position;
	}

	public void setPosition(int position) {
		this.position = position;
	}

	public int getCommandsArrayNumber() {
		return commandsArrayNumber;
	}

	public void setCommandsArrayNumber(int commandsArrayNumber) {
		this.commandsArrayNumber = commandsArrayNumber;
	}
	
}
